package ir.ninigraph.ninigraph.Model;


public class OrderEdit {

    int id, count, status, customer, image_status;
    String date, time;
    Long price;

    public int getId() {
        return id;
    }

    public int getCount() {
        return count;
    }

    public int getStatus() {
        return status;
    }

    public int getCustomer() {
        return customer;
    }

    public String getDate() {
        return date;
    }

    public String getTime() {
        return time;
    }

    public int getImage_status() {
        return image_status;
    }

    public Long getPrice() {
        return price;
    }
}
